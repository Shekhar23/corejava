package bean;

public class TreeNode {
	private String entity;
	private String [] Tages;
	private int id;
	public String getEntity() {
		return entity;
	}
	public void setEntity(String entity) {
		this.entity = entity;
	}
	public String[] getTages() {
		return Tages;
	}
	public void setTages(String[] tages) {
		Tages = tages;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
